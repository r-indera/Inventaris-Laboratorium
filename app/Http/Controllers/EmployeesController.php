    {
        // dd($request->all());
        $user = new User;
        $user->name = $request->name;
        $user->email = $request->email;
        $user->password = bcrypt('rahasia1');
        $user->role = 'staff';
        $user->remember_token = str_random(60);
        $user->save();

        $request->request->add(['user_id' => $user->id]);
        
        Employee::create($request->all());
        
        return redirect('/staff');
    }

